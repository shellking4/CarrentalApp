        <h3 style="font-weight: bold; text-align: center;"> POLITIQUES ET CONFIDENTIALITÉS CARRENTAL</h3>
        <p>
            CaRRentAL est une plateforme de location de voitures. 
            La plateforme propose plusieurs sortes de voitures, certaines à des prix 
            raisonnables et accessibles à tous, et d'autres voitures à des prix de VIP.
            
            La plateforme propose principalement deux types de services. Elle <b>loue</b>
            et <b>prête</b> des voitures à ses clients (les utilisateurs ayant un compte CaRRentAL). 
            <br>
            <h4 style="font-weight: bold; text-align: center; margin-bottom: 14px">Politique de location de voitures</h4>
            <br>
            Chaque voiture de CaRRentAL à un prix de location défini pour une journée d'utilisation de la voiture.
            Pour louer une voiture l'utilisateur va remplir un formulaire sur lequel il indique le nombre de jours pendant lesquels il souhaite 
            utiliser la voiture. La facture de location de la voiture lui est délivrée en fonction de son choix. Le clien CaRRentAL peut procéder directement au paiement des
            frais qui s'imposent sur la plateforme, si tant est qu'il a un compte bancaire ou une carte de crédit ou autre chose pouvant l'aider au paiement de ces dus à la manière 
            numérique. Le client peut aussi procéder au paiement de façon physique. Nous avons trois locaux où les clients peuvent se rendre s'il veulent effectuer leur paiement physiquement.
            Une fois les paiements réglés le client peut maintenant se rendre dans le parc CaRRentAL situé au carrefour VanderCross afin de récupérer la voiture qu'il a louée.
            Le service clientèle de CaRRentAL est consititué de charmantes personnes gentilles, très accueillantes, et très à l'écoute des clients afin de les satisfaire et les aider pour tout leur soucis.
            Une fois la durée d'utilisation de la voiture terminée, le client devra se connecter sur la plateforme et rendre la voiture là-bas avant de la ramener dans le parc CaRRentAL.
            <br>
            <h4 style="font-weight: bold; text-align: center; margin-bottom: 14px;">Politique de prêt de voitures</h4>
            <br>
            Comme dit plus haut, la plateforme CaRRentAL prend aussi en compte des prêts de voitures. Cela dit, une fois que vous avez un compte CaRRentAL, vous pouvez emprunter des voitures sur la plateforme. 
            Une voiture prêtée à un client sur CaRRentAL doit être utilisée pendant 24h au maximum, durée au bout de laquelle la voiture devra être ramenée par le client dans le parc CaRRentAL. 
            Pour pouvoir utiliser utiliser une voiture sur une plus longue durée il faut payer et la louer.
            Un client CaRRentAL ayant emprunté une voiture et qui ne l'a pas rendue au bout du temps convenu se verra dans un premier temps appelé par le service clientèle de la plateforme auquel il devra s'expliquer et fournir les détails de 
            cette dérogation à nos règles. En cas de rétention de voiture par un client CaRRentAL pendant une longue durée, une poursuite judiciaire est engagée contre le client ayant emprunté la voiture.
            Si aucune situation innattendue ne survient, alors une fois la durée d'utilisation de la voiture terminée, le client devra se connecter sur la plateforme et rendre la voiture là-bas avant de la ramener dans le parc CaRRentAL.
            <br>
            <h4 style="font-weight: bold; text-align: center; margin-bottom: 14px">Confidentialités CaRRentAL</h4>
            <br>
            Nous sommes réellement engagés dans la protection de vos données utilisateurs. Que ce soit vos mots de passe, vos numeros de cartes de crédit ou de cartes bancaires.
            Ces informations font l'objet d'une sécurité sans faille dans notre système informatique. Aucune information d'un utilisateur n'est partagée sans son approbation.
